/*
 * spfy/src/fe_host/fe_parse.c — recursive-descent parser for the
 * tagged-text FE output captured at host/PROTOCOL.md.
 *
 * SPDX-License-Identifier: GPL-3.0-or-later
 *
 * Grammar (informal, from PROTOCOL.md):
 *
 *   stream     := "%%" utterance "%%"
 *   utterance  := "#{" body "}"
 *   body       := (pause | word)*
 *   pause      := "pau(p" int ")"
 *   word       := "<" ident "(" int "," int ")" ident "," int "["
 *                 syl_chunk* "]" ">"
 *   syl_chunk  := "." int [ "," accent ] phoneme*
 *   accent     := one of  H* | L* | L+H* | H+L* | L*+H | H*+L
 *                  optionally followed by  ";" boundary
 *   boundary   := L-L% | L-H% | H-L% | H-H% | …
 *   phoneme    := ident "(" "p" int ")"
 *   ident      := [A-Za-z][A-Za-z0-9_]*
 *
 * The parser populates a flat array of `fe_parsed_phoneme_t` with the
 * full prosodic context attached (word POS, syllable stress, accent).
 * `parse_fe_output_into_slots` (in fe_host.c) then flattens this into
 * `spfy_fe_slot_t[]` with the prosody-side fields filled. ctx[5]/sp[5]
 * and CART outputs stay zero until a downstream phoneset crosswalk is
 * wired in.
 */

#include "fe_parse.h"
#include "engine_phoneid.h"   /* generated by spfy/tools/engine_phoneid_to_c.py */
#include "fe_no_refine.h"     /* generated by spfy/tools/no_refine_codegen.py */

#include <ctype.h>
#include <stdio.h>
#include <stdlib.h>
#include <string.h>

/* ============================================================
 * Tokenizer helpers (cursor-based, never look beyond .end)
 * ============================================================ */

typedef struct {
    const char *p;
    const char *end;
    int err;          /* sticky parse-error flag */
    int err_line_off; /* byte offset where the error occurred */
    const char *err_msg;
} parser_t;

static void p_skip_ws(parser_t *p) {
    while (p->p < p->end && isspace((unsigned char)*p->p)) p->p++;
}

static int p_peek(parser_t *p) {
    return (p->p < p->end) ? (unsigned char)*p->p : -1;
}

static int p_match_lit(parser_t *p, const char *lit) {
    p_skip_ws(p);
    size_t n = strlen(lit);
    if ((size_t)(p->end - p->p) < n) return 0;
    if (memcmp(p->p, lit, n) != 0) return 0;
    p->p += n;
    return 1;
}

static int p_expect_lit(parser_t *p, const char *lit) {
    if (!p_match_lit(p, lit)) {
        if (!p->err) {
            p->err = 1;
            p->err_msg = lit;
            p->err_line_off = (int)(p->p - (p->end - (p->end - p->p)));
        }
        return 0;
    }
    return 1;
}

static int p_parse_int(parser_t *p, int *out) {
    p_skip_ws(p);
    if (p->p >= p->end || !isdigit((unsigned char)*p->p)) return 0;
    int v = 0;
    while (p->p < p->end && isdigit((unsigned char)*p->p)) {
        v = v * 10 + (*p->p - '0');
        p->p++;
    }
    *out = v;
    return 1;
}

/* Identifier: starts with letter/underscore, continues with
 * alphanumeric/underscore. The hosted FE also emits apostrophes
 * inside word names (e.g. `o'clock`, `don't`, `it's`); accept those
 * as continuation chars so we don't bail mid-word.
 * Writes up to buf_sz-1 chars + NUL. Returns length copied. */
static int p_parse_ident(parser_t *p, char *buf, size_t buf_sz) {
    p_skip_ws(p);
    if (p->p >= p->end) return 0;
    int c = (unsigned char)*p->p;
    /* A byte >= 0x80 is an accented Latin-1 letter in a word name -- the
     * FE emits fr-CA/es-MX words like "días", "niño", "être" with their
     * accents intact. isalpha()/isalnum() reject those in the C locale, so
     * the tokeniser used to stop mid-word (leaving the following `(` of the
     * `(offset,len)` tag unexpected -> "error at offset N: (") and drop the
     * whole phrase. Accept high bytes as word-name letters. ASCII (en-US)
     * is unaffected. */
    if (!(isalpha(c) || c == '_' || c >= 0x80)) return 0;
    size_t n = 0;
    while (p->p < p->end) {
        c = (unsigned char)*p->p;
        if (!(isalnum(c) || c == '_' || c == '\'' || c >= 0x80)) break;
        if (n + 1 < buf_sz) buf[n] = (char)c;
        n++;
        p->p++;
    }
    if (buf_sz > 0) buf[(n < buf_sz) ? n : buf_sz - 1] = '\0';
    return (int)n;
}

/* Phoneme symbol: as p_parse_ident, plus '~'.
 *
 * fr-CA marks nasal vowels with a trailing tilde -- its phone set is
 * `... a~ e eu ... oe oe~ ox o~ ...`. Without '~' as a continuation char
 * the parser stops mid-symbol on `o~(p100)` and then fails looking for
 * '('. Kept separate from p_parse_ident so widening the phoneme charset
 * cannot change how WORD names are tokenised. en-US and es-MX symbols
 * are pure alnum and so are unaffected. */
static int p_parse_phone_ident(parser_t *p, char *buf, size_t buf_sz) {
    p_skip_ws(p);
    if (p->p >= p->end) return 0;
    int c = (unsigned char)*p->p;
    if (!(isalpha(c) || c == '_')) return 0;
    size_t n = 0;
    while (p->p < p->end) {
        c = (unsigned char)*p->p;
        if (!(isalnum(c) || c == '_' || c == '\'' || c == '~')) break;
        if (n + 1 < buf_sz) buf[n] = (char)c;
        n++;
        p->p++;
    }
    if (buf_sz > 0) buf[(n < buf_sz) ? n : buf_sz - 1] = '\0';
    return (int)n;
}

/* Accent tokens are not bare identifiers — they contain `*`, `+`, `;`,
 * `-`, `%`. Parse until we hit a non-accent char (whitespace, ` `,
 * letter prefix of phoneme, etc.). Specifically: read while the char
 * is one of [A-Za-z0-9*+;-%]. The token typically looks like:
 *   H*           pitch accent only
 *   L*           pitch accent only
 *   H*;L-L%      pitch accent + phrase + boundary
 *   L+H*         compound pitch accent
 */
static int p_parse_accent(parser_t *p, char *buf, size_t buf_sz) {
    p_skip_ws(p);
    size_t n = 0;
    while (p->p < p->end) {
        int c = (unsigned char)*p->p;
        if (isalnum(c) || c == '*' || c == '+' || c == ';' ||
            c == '-' || c == '%') {
            if (n + 1 < buf_sz) buf[n] = (char)c;
            n++; p->p++;
        } else break;
    }
    if (buf_sz > 0) buf[(n < buf_sz) ? n : buf_sz - 1] = '\0';
    return (int)n;
}

